package com.webmobrilweatherapp.model;

public class ListModel {
    int imageview;
    int image;
    int imageviews;


    public int getImageview() {
        return imageview;
    }

    public int getImage() {
        return image;
    }

    public int getImageviews() {
        return imageviews;
    }

    public ListModel(int imageview, int image, int imageviews) {
        this.imageview = imageview;
        this.image = image;
        this.imageviews = imageviews;
    }



}
